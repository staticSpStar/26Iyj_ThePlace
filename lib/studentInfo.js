export function getStudentInfoByEmail(email) {
  const info = STUDENT_INFO[email];

  if (!info) {
    return {
      name: email || "알 수 없음",
      studentId: null,
      grade: "미상",
      classNo: "미상",
      classLabel: "미상"
    };
  }

  const grade = info.studentId?.[0] || "미상";
  const classNo = info.studentId?.[1] || "미상";

  return {
    ...info,
    grade,
    classNo,
    classLabel: grade !== "미상" && classNo !== "미상"
        ? `${grade}학년 ${classNo}반`
        : "미상"
  };
}